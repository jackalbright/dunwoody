    <h1 class='withSubhead'>Why Dunwoody?<br>&nbsp;&nbsp;It’s About the Details</h1>
 

<p>Those of us living in the Delaware Valley have the benefit of a large number—and variety—of senior living communities. Often, one of my friends, who know of my association with Dunwoody, will ask “what’s the best choice?” When I respond “Dunwoody”, they ask “why?” 
</p>
<h3>Stability and Transparency</h3>
<p>I usually start by dividing the universe of senior living communities into two categories: Continuing Care Retirement Communities (CCRCs) and “All Others.” The CCRCs, like Dunwoody, provide a continuous-living environment, from independent living through whatever care may be needed as an individual ages. Because CCRCs operate on sound actuarial principles, they are able to offer lifetime care under a single and low-cost fee structure. </p>
<p>The “All Others” category includes the many communities that do not provide for one’s future healthcare needs under a single fee structure and do not operate under actuarial principles. They are usually less costly at the outset, but when personal care or skilled nursing is needed, the cost can be unpredictable, unaffordable—even devastating. So when advising friends, I typically dismiss the “All Others” category as being too risky, too insecure, and, ultimately, an imprudent choice.</p>

<p>This leaves the CCRCs as the better option. But I also further distinguish among CCRCs: those operated by 
for-profit organizations and those that are not-for-profit. In my opinion, the choice is obvious. Do you want a portion of your fees to be sent forward to some corporation and paid out as bonuses and dividends, or would you prefer that the fees stay wholly within the community to support the Residents’ services? I prefer the latter.</p>

<p>Finally, I make a distinction between those communities that are transparent in providing complete audited financial statements, and those that are not so forthcoming, or that provide statements combining the finances of more than one community in a single report. Without precise financial disclosure regarding the specific community to be evaluated, I do not think it is possible to come to a reasonable judgment regarding the organization’s financial viability. I typically reject those communities that are not up front in providing this data. </p>

<h3>The Dunwoody Difference</h3>
<p>Within a 15-mile radius of Dunwoody, there are seven not-for-profit CCRCs. All, including Dunwoody, offer superb living and dining services, a variety of social, cultural and recreational activities, and the full continuum of care. They are well-run, and offer a wide range of lifestyle options to their Residents. So why, from among these excellent communities, would one choose Dunwoody?</p>
<p>Well, Dunwoody has a long and successful history embodying a firm commitment to excellence in all phases of its operations. Dunwoody is more established than the newer communities offering a resort-like atmosphere. However, as one looks beneath the surface, it becomes increasingly evident that Dunwoody is the clear choice. This, of course, is my own personal opinion, but it is based on an analysis of those critical details which I think are the most important: 

<ul>
<li>Financial Stability</li>
<li>Quality of Healthcare</li>
<li>Comfort and Convenience</li>
</ul>
</p>

<h3>Financial Stability</h3>
<p>
Starting with an examination of the community’s financial stability, the first item that I would review is the long-term debt. Dunwoody has a strong and stable balance sheet with long-term debt of only $22.0 million. The average long-term debt for our peer group is $34.8 million. In addition, unlike some of our peers, Dunwoody’s debt is viewed by outside agencies as being of very high quality. This is demonstrated by our BBB+ investment-grade rating recently confirmed by Standard & Poor’s. 
</p>

<p>
Since our founding, Dunwoody has invested wisely in management programs and facilities. Dunwoody has planned for the future by establishing certain funds internally designated by the Board of Trustees that are not restricted and are generally available for any purpose that the Board should determine to be in the best interest of the community. This might include such things as:
<ul>
<li>Making improvements to buildings and grounds.</li>
<li>Renovating common spaces.</li>
<li>Maintaining infrastructure.</li>
<li>Providing a cash cushion when economic conditions become unfavorable.</li>
</ul>
</p>

<p>
As of December 31, 2010, Dunwoody’s internally designated funds were $15.1 million, while the average of similar funds for our peers, as of their most recently available public statements, was $8.4 million. But more noteworthy, when considering the available funds per independent living unit (ILU), Dunwoody stands alone with $60 thousand/ILU while the average for its peers is only $28 thousand/ ILU. On this basis, Dunwoody has more than double the amount of such funds as its peers, and it is this fact, along with Dunwoody’s lower long-term debt, that provides me with peace of mind. As a child of the Depression, I am ever mindful of the risks of future inflation, deflation, and economic volatility. Dunwoody’s strong financial condition is, for me, a critical factor when considering where I will spend the rest of my life. 
</p>

<h3>Quality of Healthcare</h3>
<p>
After evaluating a community’s financial condition, the second of the “critical factors” that I would review is the quality of—and access to—healthcare services. After all, the principal reason for choosing a CCRC is the availability of a full continuum of care. One should expect that the community will be able to meet its lifetime commitment to its Residents with ease. 
</p>

<p>This is a complex subject with a number of elements to consider. Among these are the types of accommodations and the range of services available on site. In Dunwoody’s Care Center, care is provided only in single rooms. There are no doubles or triples—unlike some of our peers. Dunwoody also has separate units for the memory impaired. Finally, Dunwoody has a physically separate and professionally staffed Short Term Rehab unit that provides a level of care not generally available in other CCRC’s.
</p>
<p>Dunwoody’s healthcare is a complete package—allowing you to have peace of mind when planning for this phase of life. However, another way to think about the healthcare component is to look at the relationship between the number of healthcare beds and independent living units. This addresses the question as to whether the size of the healthcare center is adequate to serve the needs of the community’s Residents. In this comparison, Dunwoody truly excels. With 162 Care Center rooms in support of 253 independent living units, Dunwoody offers one 
healthcare room for every 1.6 independent living units. For the peer group, the average is one healthcare bed for every 2.4 independent living units. This is a cardinal element of care, and on this measure, Dunwoody has 50% more capacity than the peer average.</p>

<h3>Comfort and Convenience</h3>
<p>Finally, in addition to considerations of financial viability and quality healthcare, the third “critical factor” that I consider significant in evaluating a CCRC is the comfort and convenience experienced by the Residents. I look at the way the community anticipates—and mitigates—the disruptive inconveniences of everyday living. 
</p>
<p>By their very nature, all CCRCs attempt to do this and many do it with great success. However, Dunwoody takes it a step further. For example, alone among our peers, Dunwoody provides heated enclosed walkways from all living units to the common areas where dining, recreational, and social activities take place. Moreover, these enclosed walkways are not just for the apartments, but for the Country Houses (cottages) as well. Thus, at Dunwoody, all its Residents are free from the hassle of ever having to go out in the rain, sleet, and snow. While this may be relatively unimportant to people in their 70’s, it becomes increasingly essential as the years go by. It is important not only as a matter of convenience, but also as a matter of personal safety.
</p>
<p>Another aspect of comfort and convenience has to do with electrical service. In 2010, Dunwoody initiated a substantial project to upgrade both its internal power grid and emergency power capabilities. And, consistent with our goal to provide a trouble-free environment, the project includes an on-site generator to serve the entire community. This means that in the future, when electrical outages occur, Dunwoody Residents will not experience the inconvenience of diminished heat, light, and power. 
</p>
<p>In addition to the extraordinary social, educational, and cultural aspects of Dunwoody living, these observations are a summary of the principal characteristics of Dunwoody which, in my opinion, put this community on a level far exceeding its peers. 
</p>
<p>So - why Dunwoody? As I previously noted, and as I explain to my enquiring friends, all of Dunwoody’s peers are wonderful communities. 
</p>
<p>
But Dunwoody is a little bit better at providing the things that really matter. 
</p>
 
