<p>
<a name="q1"><span class="QA">Q.</span> </a>  <span class="question">Is the community financially stable?</span><br>
<span class="QA">A.</span> Dunwoody Village, a not-for-profit Continuing Care Retirement Community, has an investment grade rating from Standard & Poor's.
</p>


<p>
<a name="q2"><span class="QA">Q.</span> </a> <span class="question">How long have you been in operation?</span><br>
<span class="QA">A.</span> Dunwoody Village opened in 1974. The Dunwoody Home opened in 1924.
</p>

<p>
<a name="q3"><span class="QA">Q.</span> </a> <span class="question">Is there a Residents' Reserve Fund?</span><br>
<span class="QA">A.</span> Yes. The fund is used to subsidize residents who are unable to pay their monthly fee through no fault of their own.
</p>

<p>
<a name="q4"><span class="QA">Q.</span> </a> <span class="question">What lifestyle choices are available?</span><br>
<span class="QA">A.</span> Three levels of care are available: Residential Living, Personal Care, and Skilled Nursing Care. There are two separate memory support units, one in Personal Care and one in Skilled Nursing Care. We also offer a dedicated Rehab unit, which is available to both residents and non-residents alike.</p>

<p>
<a name="q5"><span class="QA">Q.</span> </a> <span class="question">How many hands-on nursing hours are provided, per patient, per day?</span><br>
<span class="QA">A.</span> Dunwoody Village has more than four hands-on nursing hours per resident in the Care Center per day, which exceeds the state requirement. 
</p>

<p>
<a name="q6"><span class="QA">Q.</span> </a> <span class="question">What is the LeadingAge Quality First program?</span><br>
<span class="QA">A.</span> LeadingAge is a government initiative that helps organizations achieve excellence in aging services, and earn public trust. Dunwoody has pledged to work in partnership with the government and consumers to create an environment of quality care and service. LeadingAge's ten elements of quality include: commitment, governance and accountability, leading-edge care and services, community involvement, continuous quality improvement, human resources development, consumer-friendly information, consumer participation, research findings and education, and public trust and confidence.</p>

<p>
<a name="q7"><span class="QA">Q.</span> </a> <span class="question">Is there a waiting list? If so, how long?</span><br>
<span class="QA">A.</span> We have a priority list but usually have availability ranging from immediate to three years, depending on the size of the residence.
</p>

<p>
<a name="q8"><span class="QA">Q.</span> </a> <span class="question">What is the refund policy for deposits and entry fees?</span><br>
<span class="QA">A.</span> The priority list deposit is $1,300 for one person; $1,400 for two people. $1,000 is refundable or goes toward the entrance fee; the $300 or $400 is a processing fee. For that priority list deposit, the prospective resident may use the fitness center with trainer, and the swimming pool with lifeguard. We also send a monthly calendar that lists programs and selected events open to future residents. We have two types of entrance fees. The non-refundable entrance fee is a one-time fee that reduces by 2% per month for 50 months. The 50% refundable entrance fee is 46% higher than the non-refundable, but the resident or resident's estate always recovers 50% of the original entrance fee.
</p>

<p>
<a name="q9"><span class="QA">Q.</span> </a> <span class="question">Is any portion of the entry fee and monthly fee tax-deductible?</span><br>
<span class="QA">A.</span> A substantial portion of the entrance and monthly fees are tax-deductible as a medical expense on the resident's federal income tax return. Approximately 33% of the entrance fee (one time only, for the year paid), and 33% of the monthly maintenance fee (annually) can be taken as deductions. These are just averages from the past five years, and the actual percentage may vary.
</p>

<p>
<a name="q10"><span class="QA">Q.</span> </a> <span class="question">Does the monthly fee increase when a resident goes to the Healthcare Center?</span><br>
<span class="QA">A.</span>The only increase is for additional meals. One daily meal is included in the monthly fee. In the Care Center, residents are required by law to be served three meals a day.
</p>

<p>
<a name="q11"><span class="QA">Q.</span> </a> <span class="question">What services are included in the monthly fee?</span><br>
<span class="QA">A.</span> Please refer to the <a href="amenities.php">Amenities page</a>.</p>

<p>
<a name="q12"><span class="QA">Q.</span> </a> <span class="question">How many meals are included?</span><br>
<span class="QA">A.</span> One meal a day is included in the monthly fee. (Most residents choose dinner.) Additional meals are available for a monthly fee.</p>

<p>
<a name="q13"><span class="QA">Q.</span> </a> <span class="question">What medical costs does the contract cover?</span><br>
<span class="QA">A.</span> Dunwoody Village covers routine nursing and personal care services.
</p>

<p><a name="q14"><span class="QA">Q.</span> </a> <span class="question">Is the management open and responsive to resident concerns?</span><br>
<span class="QA">A.</span> Yes, the managers at Dunwoody Village have an open-door policy.
</p>

<p>
<a name="q15"><span class="QA">Q.</span> </a> <span class="question">Is free transportation provided to needed services?</span><br>
<span class="QA">A.</span> Yes. Free transportation is provided to local doctors' offices, religious services and grocery stores twice a week and to shopping every other week.
</p>
<p><a name="q16"><span class="QA">Q.</span> </a> <span class="question">Is the community conveniently located and close to public transportation?</span><br>
<span class="QA">A.</span> Yes. Dunwoody Village is on a public transportation route, and five miles from the Bryn Mawr train station. The Village's paved path connects to an adjacent shopping center with approximately 30 stores.</p>

<p>
<a name="q17"><span class="QA">Q.</span> </a> <span class="question">What services do you offer for those who are not Dunwoody Village residents?</span><br>
<span class="QA">A.</span> When space is available, Dunwoody Village offers several services, including sub-acute rehabilitation services, personal care, skilled nursing, memory support care, and respite care.
</p>

 
