<h3>Volunteer Opportunities</h3>

<p>Volunteers play a valuable part in enriching the lives of Dunwoody Village residents and they bring a unique energy to the entire community. By contributing time, special skills, and a caring heart you can make a difference and enhance the quality of life of older adults. 
</p>

<p>At Dunwoody Village, volunteers have the opportunity to make new friends and to encounter new experiences, skills and challenges. Most volunteers commit to an ongoing number of hours per week or per month. Volunteering is an excellent way to brush up on job skills before returning to the workforce. We also encourage high school and college students to consider us for a work experience internship.
</p>

