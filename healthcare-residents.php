Living well means access to excellent healthcare, and Dunwoody Village's award-winning program is unparalleled. A personalized wellness plan helps each resident feel their best and the Village offers a full spate of available services, including 24-hour emergency assistance when needed. In addition, residents can take advantage of onsite providers, including primary care physicians, specialists and a dentist. If residents choose to stick with their own physicians, transportation is always available. Dunwoody Village is located less than a mile away from Main Line Health Center in Newtown Square, where many types of outpatient imaging and diagnostic services are available.  
