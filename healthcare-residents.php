<h3>Personal Care Accommodations</h3>
<p>Our Personal Care Accommodations combine the freedom and privacy of apartment living with round-the-clock nursing care. Personal Care Accommodations are organized in “neighborhoods” of six or seven private residences—each with unique, tasteful décor—clustered around a common reception area, kitchen, dining area, library and gathering room.
</p>
<h3>Skilled Nursing Accommodations</h3>
<p>Our Skilled Nursing Accommodations offer 24-hour onsite medical supervision from our friendly, caring staff. On average, staff spends an average of four hours a day with each patient –well above state requirements. Residents are assisted with daily tasks such as bathing, dressing, medication management and dining as necessary. With our indoor walkways, residents in Skilled Nursing Accommodations can easily access the same amenities and activities as their friends in Personal Care and private residences, contributing to better mental and physical health and a more positive aging experience. 
</p>
<h3>Memory Support Accommodations</h3>
<p>The Memory Support Unit in Cedars West is dedicated to residents dealing with the physical and mental challenges of early-stage dementia and Alzheimer’s disease. In this dedicated Personal Care wing, emphasis is placed on 24-hour care, physical and cognitive therapy, and a secure, monitored environment, while enclosed gardens, walkways and safe areas are provided for outdoor enjoyment. As in our Skilled Nursing Accommodations, residents receive assistance with daily tasks such as bathing, dressing, medication management and dining. 
</p>



