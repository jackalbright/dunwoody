<h1 >Our History </h1>
<p class='bigFirst'>In the 1800s, during an apprenticeship among Quaker families, William Hood Dunwoody learned a respect for life and an appreciation for all mankind. He carried these ideals throughout his life and later founded a home for older adults on his property. In 1974, this property became Dunwoody Village, one of the area’s first continuing care retirement communities (CCRCs).</p>
<p> In the early days, Dunwoody was home to just 50 residents; today, our numbers total 400, including many second-generation residents. They come here because they recognize our excellent reputation and our continued commitment to Mr. Dunwoody’s ideals of caring, respect and kindness.
</p>
