<h3  >The Octagonal School House</h3>
<p>Dunwoody Village is home to an historic octagonal schoolhouse called the Hood Octagonal School. Built in 1842 by William Hood Dunwoody’s great grandfather James Hood, the schoolhouse was used well into the 19th century. In 2006, it was awarded designation on the National Register of Historic Places. Today, it serves local schoolchildren as an historic learning site.     
</p>
